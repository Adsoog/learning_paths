package main

import "fmt"

func main() {
    a := 10
    b := 20

    // Ejemplos de uso de operadores de comparaci√≥n
    fmt.Println(a == b) // Falso, a no es igual a b
    fmt.Println(a != b) // Verdadero, a no es igual a b
    fmt.Println(a < b)  // Verdadero, a es menor que b
    fmt.Println(a <= b) // Verdadero, a es menor o igual que b
    fmt.Println(a > b)  // Falso, a no es mayor que b
    fmt.Println(a >= b) // Falso, a no es mayor o igual que b

	// ==	Igual a
	// !=	No es igual a
	// <	Menor que
	// <=	Menor o igual que
	// >	Mayor que
	// >=	Mayor o igual que
}